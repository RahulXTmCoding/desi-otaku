/** Pick a random element from an array */
const pick = <T>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

/** Shuffle an array (Fisher–Yates) and return a new copy */
export const shuffle = <T>(arr: T[]): T[] => {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
};

export interface FakePurchase {
  buyerName: string;
  city: string;
  /** Minutes ago (5 – 180) */
  minutesAgo: number;
  /** Human-readable time string */
  timeAgoLabel: string;
}

/** Format a minutes-ago offset into a readable label */
const formatTimeAgo = (minutes: number): string => {
  if (minutes < 60) return `${minutes} minutes ago`;
  const hours = Math.floor(minutes / 60);
  if (hours === 1) return "an hour ago";
  return `${hours} hours ago`;
};

/**
 * Generate one fake purchase entry.
 * Each call produces fresh random values.
 */
export const generateFakePurchase = (): FakePurchase => {
  const minutesAgo = Math.floor(Math.random() * 175) + 5; // 5 – 180 min
  return {
    buyerName: `${pick(FIRST_NAMES)} ${pick(LAST_NAMES)}`,
    city: pick(CITIES),
    minutesAgo,
    timeAgoLabel: formatTimeAgo(minutesAgo),
  };
};

/**
 * Generate `count` fake purchase entries at once.
 * Useful for seeding the carousel in a single useMemo call.
 */
export const generateFakePurchases = (count: number): FakePurchase[] =>
  Array.from({ length: count }, () => generateFakePurchase());
